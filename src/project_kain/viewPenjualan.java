    public void hapusTransaksi() {
        // Mendapatkan model tabel
        DefaultTableModel model = (DefaultTableModel) tablelist.getModel();

        // Mendapatkan baris yang dipilih
        int selectedRow = tablelist.getSelectedRow();

        if (selectedRow >= 0) {
            // Mengambil nilai total bayar dari baris yang dipilih
            Object value = model.getValueAt(selectedRow, 5); // Ambil nilai di kolom ke-5 (indeks 5)

            // Konversi nilai ke Integer
            int total_bayar_item = (int) Float.parseFloat(value.toString());

            // Mengurangi total kumulatif
            total_yg_hrs_dibayar -= total_bayar_item;

            // Memperbarui label Total Bayar
            TFTotalBayar.setText("Rp. " + total_yg_hrs_dibayar);

            // Menghapus baris dari model tabel
            model.removeRow(selectedRow);

            // Memberikan notifikasi sukses
            JOptionPane.showMessageDialog(null, "Transaksi berhasil dihapus!");
        } else {
            JOptionPane.showMessageDialog(null, "Pilih transaksi yang ingin dihapus terlebih dahulu.");
        }
}

    
