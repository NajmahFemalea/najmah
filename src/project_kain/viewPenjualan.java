    private void BTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTambahActionPerformed

        String jenisBrg = TFJenisBrg.getSelectedItem().toString(); 
        int jmlh = Integer.parseInt(TFJmlh.getText()); 
        String alamat = TFAlamat.getText(); 
        int harga = Integer.parseInt(TFHarga.getText()); 
        String pembayaran = TFPembayaran.getSelectedItem().toString(); 
        int total_bayar = jmlh * harga; 
        total_yg_hrs_dibayar += total_bayar;
        
        TFTotalBayar.setText("Rp. " + total_yg_hrs_dibayar);
        
        // Memasukkan data ke dalam ArrayList menggunakan ControllerBarang
        brg.InsertData(jenisBrg, jmlh, alamat, harga, pembayaran, total_bayar);

        // Menampilkan data pada tabel
        tablelist.setModel(brg.showData());
    }//GEN-LAST:event_BTambahActionPerformed

